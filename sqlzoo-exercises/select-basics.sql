-- โครงสร้างพื้นฐานที่จดจำ:
-- SELECT : เลือก คอลัมน์ที่ต้องการโชว์
-- FROM   : จาก ตารางไหน
-- WHERE  : ที่ไหน (เงื่อนไขที่อยากให้คัดกรอง + สูตร Query อื่นๆ)

-- --------------------------------------------------------
-- 1. Introducing the world table of countries
-- โจทย์: ให้แก้ไขโค้ดเพื่อแสดงจำนวนประชากร (population) ของประเทศ Germany
-- เทคนิคจากลายมือ: เลือกเฉพาะคอลัมน์ `name` (หรือ `population`) ที่มีค่าเท่ากับ 'Germany'
-- --------------------------------------------------------

SELECT name, population 
FROM world
WHERE name = 'Germany';


-- --------------------------------------------------------
-- 2. Scandinavia
-- โจทย์: แสดงชื่อ (name) และประชากร (population) ของประเทศ 'Sweden', 'Norway' และ 'Denmark'
-- เทคนิคจากลายมือ: การใช้คำสั่ง IN เพื่อ "Checking a list" ช่วยให้เราตรวจสอบได้ง่ายๆ ว่าข้อมูลอยู่ในเซ็ตที่เรากำหนดไว้หรือไม่
-- --------------------------------------------------------

SELECT name, population 
FROM world
WHERE name IN ('Sweden', 'Norway', 'Denmark');


-- --------------------------------------------------------
-- 3. Just the right size
-- โจทย์: แสดงประเทศและพื้นที่ (area) ที่มีขนาดอยู่ระหว่าง 200,000 ถึง 250,000 ตร.กม.
-- เทคนิคจากลายมือ: คำสั่ง BETWEEN หมายถึง "อยู่ระหว่าง" 
-- 🚨 จุดคำนวณที่ต้องระวังมาก!! เลข '0' หลักหน่วย/หลักสิบ ห้ามเขียนขาดหรือเกินเด็ดขาด มูลค่าตัวเลขเปลี่ยนไปไม่ตรงกับโจทย์จะทำให้เกิด Error ทันที!
-- --------------------------------------------------------

SELECT name, area 
FROM world
WHERE area BETWEEN 200000 AND 250000;


-- --------------------------------------------------------
-- 🌟 Extra Insights & Bonus Exercises จากสมุดจดสรุป
-- --------------------------------------------------------

-- [Extra 1] การใช้ LIKE ค้นหาคำที่ขึ้นต้นด้วย 'Al'
-- เทคนิค: ใช้ในการค้นหาข้อความในคอลัมน์ `name` ที่มีคำว่า 'Al' เป็นส่วนประกอบ
-- โดยตัวอักษรต่อจากนั้นจะเป็นอะไรก็ได้ ให้แทนค่าตำแหน่งที่ตามหลังด้วยเครื่องหมาย "%"
-- เงื่อนไขเพิ่มเติมที่จดไว้:
-- AND : เงื่อนไขหลังต้องถูกทั้งหมด
-- OR  : เงื่อนไขกลุ่มใดกลุ่มหนึ่งถูกก็พอ
-- NOT : ไม่เอาที่เข้าคู่

SELECT name, area 
FROM world
WHERE name LIKE 'Al%';


-- [Extra 2] การวัดความยาวของข้อความ (Length of Strings)
-- เทคนิค: ฟังก์ชันนี้จะแตกต่างกันไปตามแต่ละระบบฐานข้อมูล (Database Engine)
-- - สำหรับ MySQL, PostgreSQL, Oracle -> จะใช้คำสั่ง `Length(name)`
-- - สำหรับ Microsoft SQL Server     -> จะใช้คำสั่ง `LEN(name)`
-- โจทย์เสริม: ค้นหาประเทศในทวีปยุโรปที่มีความยาวของชื่อเท่ากับ 5 ตัวอักษร

SELECT name, LENGTH(name)
FROM world
WHERE LENGTH(name) = 5 
  AND region = 'Europe';


-- [Extra 3] การคำนวณความหนาแน่นของประชากร (Population Density)
-- เทคนิค: Density = ความหนาแน่นของประชากรต่อพื้นที่ สามารถเขียนสูตรคำนวณใน SELECT ได้ทันที
-- โจทย์: แสดงความหนาแน่นของประเทศ China, Australia, Nigeria และ France

SELECT name, area/population AS density
FROM world
WHERE name IN ('China', 'Australia', 'Nigeria', 'France');
-- [Extra 4] เจาะลึกฟังก์ชันวัดความยาวข้อความ (String Length Functions)
-- เทคนิคสำคัญ: ในฐานะ Data Engineer เราต้องรู้ว่าฟังก์ชันจัดการข้อมูลบางตัวจะเปลี่ยนไปตาม Database Engine ที่ใช้
-- จากสรุปภาพ:
--   - หากใช้ MySQL, PostgreSQL, Oracle       -> จะใช้คำสั่ง `LENGTH(column_name)`
--   - หากใช้ Microsoft SQL Server (T-SQL)   -> จะใช้คำสั่ง `LEN(column_name)`
-- โจทย์ประยุกต์: ค้นหาประเทศในทวีปยุโรป (region = 'Europe') ที่มีความยาวของชื่อประเทศเท่ากับ 5 ตัวอักษรพอดี

SELECT name, LENGTH(name) AS name_length
FROM world
WHERE LENGTH(name) = 5 
  AND region = 'Europe';

-- 💡 ผลลัพธ์ที่ได้จากการ Query (Data Output Sample):
-- name     | name_length
-- ---------|------------
-- Italy    | 5
-- Malta    | 5
-- Spain    | 5


-- [Extra 5] ทริคการกรองข้อมูลแบบมีเงื่อนไขร่วม (AND, OR, NOT Logic)
-- จากสรุปเลกเชอร์ ความหมายและการทำงานของคำสั่งควบคุมเงื่อนไขในตารางข้อมูล:
--   - AND : เงื่อนไขรอบข้าง "ต้องเป็นจริงทั้งหมด" ข้อมูลถึงจะแสดงออกไป
--   - OR  : เงื่อนไข "อย่างใดอย่างหนึ่งเป็นจริง" ก็เพียงพอแล้ว
--   - NOT : ใช้สำหรับ "ไม่เอาเงื่อนไขนี้" หรือกลับค่าจากจริงเป็นเท็จ


-- [Extra 6] ควิซประยุกต์ระบุตัวตนประเทศ (Case Study: Andorra Analysis)
-- โจทย์: ค้นหาข้อมูลพื้นที่ (area) จากตาราง world โดยมีเงื่อนไขว่าประเทศนั้นต้องมี population = 64,000
-- จากการวิเคราะห์ตัวเลือกในสมุดจดสรุป:
--   A. Andorra 234
--   B. Andorra 468
--   C. Andorra 936  <-- คำตอบที่ถูกต้องตามโครงสร้างตารางและเงื่อนไขที่กำหนด

SELECT name, area 
FROM world
WHERE population = 64000;


-- 🚨 [DE MUST KNOW] สรุปข้อควรระวังหลัก (Key Takeaways จากบทเรียน SELECT Basics)
-- 1. การจัดการเลขศูนย์ '0' ในคำสั่ง BETWEEN:
--    เวลาพิมพ์กำหนดช่วงตัวเลข เช่น `BETWEEN 200000 AND 250000` ต้องเช็กจำนวนเลข 0 ให้ดี 
--    หากพิมพ์ตกหรือเกินไปเพียงตัวเดียว จะทำให้ Logic การกรองช่วงข้อมูล (Data Range) เพี้ยนทันที 
--    และระบบจะแจก Error หรือดึงข้อมูลมาผิดชุด (ซึ่งอันตรายมากในงาน Pipeline)
