-- --------------------------------------------------------
-- Quiz 1: Albert Einstein Prize
-- โจทย์: แสดงปี (yr) และสาขา (subject) ที่ 'Albert Einstein' ได้รับรางวัล
-- 🚨 [ERROR WATCH] จุดตกม้าตาย:
--    - เขียนผิด: WHERE subject = 'Albert Einstein' (ผิด! เพราะชื่อคนอยู่ในคอลัมน์ winner ไม่ใช่ subject)
--    - วิธีแก้ไข: ต้องเช็ก Metadata หรือคำอธิบายตารางก่อนกรองข้อมูลเสมอ
-- --------------------------------------------------------

SELECT yr, subject 
FROM nobel
WHERE winner = 'Albert Einstein';


-- --------------------------------------------------------
-- Quiz 2: Peace Winner Since 2000
-- โจทย์: แสดงชื่อผู้ได้รับรางวัลสาขา 'Peace' ตั้งแต่ปี 2000 เป็นต้นไป (รวมปี 2000)
-- 🚨 [SYNTAX ERROR WATCH]:
--    - เขียนผิด: WHERE winner = 'Peace' AND yr >= 2000
--    - ผลลัพธ์: SQL ERROR: You have an error in your SQL syntax...
--    - วิธีแก้ไข: ต้องจับคู่เงื่อนไขคอลัมน์ให้ถูก 'Peace' คือสาขา ต้องใช้ `subject = 'Peace'`
-- --------------------------------------------------------

SELECT winner
FROM nobel
WHERE subject = 'Peace' 
  AND yr >= 2000;


-- --------------------------------------------------------
-- Quiz 3: Literature Winners (1980 - 1989)
-- โจทย์: แสดงรางวัลสาขา Literature ทั้งหมดในช่วงปี 1980 ถึง 1989 (inclusive)
-- เทคนิค: การระบุช่วงเวลาโดยใช้ตรรกะคำสั่ง AND เชื่อมโยงช่วงปีเข้าด้วยกัน
-- --------------------------------------------------------

SELECT yr, subject, winner
FROM nobel
WHERE subject = 'Literature'
  AND yr >= 1980
  AND yr <= 1989;


-- --------------------------------------------------------
-- Quiz 4: Presidential Winners Case Study
-- โจทย์: แสดงรายละเอียดทั้งหมดของรางวัลที่ผู้ได้รับเป็นประธานาธิบดี (เช่น Theodore Roosevelt)
-- เทคนิควิเคราะห์จากลายมือ: โจทยบอกให้หา "รายชื่อผู้ที่ได้เป็นประธานาธิบดี" 
--                        แต่ถ้าเราไปกรองข้อมูลคอลัมน์ด้วยคำว่า 'peace' จะไม่ถูก 
--                        เพราะประธานาธิบดีบางคนอาจจะได้สาขาอื่น ดังนั้นต้องเจาะจงที่รายชื่อบุคคลโดยตรงด้วยคำสั่ง IN
-- --------------------------------------------------------

SELECT yr, subject, winner 
FROM nobel
WHERE winner IN ('Theodore Roosevelt', 'Woodrow Wilson', 'Jimmy Carter', 'Barack Obama');


-- --------------------------------------------------------
-- Quiz 5: Physics & Chemistry (Complex Condition Logic)
-- โจทย์: แสดงรางวัลสาขา Physics ปี 1980 ร่วมกับสาขา Chemistry ปี 1984
-- 🚨 [LOGIC ERROR WATCH]:
--    - หากเขียนเงื่อนไขเรียงกันแบบไม่มีวงเล็บ (ภาพซ้าย) ระบบจะตีความหมายเงื่อนไขเพี้ยน ทำให้ได้ข้อมูลปีอื่นหลุดมาด้วย
--    - วิธีแก้ไข (ภาพขวา): ใช้เครื่องหมายวงเล็บ ( ) มาช่วยจำกัดคำสั่งในการคํานวณเงื่อนไข เพื่อแบ่งกลุ่ม Logic ให้ชัดเจน
-- --------------------------------------------------------

SELECT yr, subject, winner
FROM nobel
WHERE (subject = 'Physics' AND yr = 1980)
   OR (subject = 'Chemistry' AND yr = 1984);


-- --------------------------------------------------------
-- Quiz 6: Excluding Subject using NOT IN
-- โจทย์: แสดงรางวัลปี 1980 โดยไม่เอาสาขา Chemistry และ Medicine
-- 🚨 เทคนิคสำคัญจากลายมือ: 
--    - ปกติเรามักคุ้นเคยกับการใช้ `IN` ในการหาชุดข้อมูลที่ต้องการ
--    - แต่เมื่อต้องการ "ยกเว้นกลุ่มข้อมูล" ให้สลับมาใช้คำสั่ง `NOT IN` นำหน้าวงเล็บชุดข้อมูลทันที เข้าใจง่ายและโค้ดสะอาดกว่าการใช้ != ซ้อนกันหลายตัว
-- --------------------------------------------------------

SELECT yr, subject, winner
FROM nobel
WHERE yr = 1980
  AND subject NOT IN ('Chemistry', 'Medicine');
