-- --------------------------------------------------------
-- Quiz 1: Per Capita GDP
-- โจทย์: แสดง per capita GDP ของประเทศที่มีประชากร (population) อย่างน้อย 200 ล้านคน
-- เทคนิคจากลายมือ: การคำนวณสัดส่วนโดยใช้เครื่องหมาย Math Operation (gdp / population) 
-- --------------------------------------------------------

SELECT name, gdp/population 
FROM world
WHERE population >= 200000000;


-- --------------------------------------------------------
-- Quiz 2: Population in Millions
-- โจทย์: แสดงชื่อประเทศและประชากรในหน่วย "ล้านคน" สำหรับประเทศในทวีป South America
-- เทคนิคจากลายมือ: แปลงหน่วยตัวเลขจากหน่วยเดี่ยวให้เป็นหน่วยล้านโดยหารด้วย 1000000 (population / 1000000)
-- --------------------------------------------------------

SELECT name, population/1000000 
FROM world
WHERE continent = 'South America';


-- --------------------------------------------------------
-- Quiz 3: Exclusive OR (XOR)
-- โจทย์: ค้นหาประเทศที่มีพื้นที่ > 3 ล้าน ตร.กม. "หรือ" มีประชากร > 250 ล้านคน แต่ "ไม่เอาทั้งสองเงื่อนไขพร้อมกัน"
-- 🚨 [DE MUST KNOW] คีย์เวิร์ดระดับเทพ: XOR (อย่างใดอย่างหนึ่ง แต่อย่าซ้ำ!)
-- เปรียบเทียบตารางความจริง (Logic Truth Table) จากลายมือสรุป:
--   - คำสั่ง OR  = เงื่อนไข A จริง, เงื่อนไข B จริง -> ผลลัพธ์เป็น "จริง" (เอาทั้งหมด)
--   - คำสั่ง XOR = เงื่อนไข A จริง, เงื่อนไข B จริง -> ผลลัพธ์เป็น "เท็จ" (ถ้าจริงซ้อนกัน...ไม่เอา!)
--   * ตัวอย่างเช่น China หรือ USA ที่มีทั้งพื้นที่และประชากรเข้าเกณฑ์ทั้งคู่ จะถูกคัดออกด้วย XOR
-- --------------------------------------------------------

SELECT name, population, area
FROM world
WHERE population > 250000000 XOR area > 300000000;


-- --------------------------------------------------------
-- Quiz 4: Data Formatting with ROUND (Millions and Billions)
-- โจทย์: แสดงชื่อ, ประชากรในหน่วยล้านคน และ GDP ในหน่วยพันล้านคน สำหรับทวีป South America 
--       โดยปัดเศษทศนิยมให้เหลือ 2 ตำแหน่ง
-- เทคนิคจากลายมือ: ฟังก์ชัน ROUND(ค่าข้อมูล, จำนวนตำแหน่งทศนิยมที่ต้องการ)
-- --------------------------------------------------------

SELECT name, 
       ROUND(population/1000000, 2) AS pop_in_millions,
       ROUND(GDP/1000000000, 2) AS gdp_in_billions
FROM world
WHERE continent = 'South America';


-- --------------------------------------------------------
-- Quiz 5: Per-Capita GDP Rounding (Negative Decimals Trick)
-- โจทย์: แสดงชื่อและ per-capita GDP ของประเทศที่มี GDP อย่างน้อย 1 ล้านล้าน (1 Trillion) 
--       โดยปัดเศษตัวเลขให้อยู่ในหลักพันที่ใกล้ที่สุด (Nearest 1000)
-- 🚨 จุดสังเกตเด่นมากจากลายมือ: การใส่ค่าติดลบใน ROUND เช่น ROUND(value, -3)
--   - ปกติเลขบวกคือปัดเศษทศนิยมหลังจุด
--   - แต่ถ้าใส่ติดลบ "-3" ตัวระบบจะทำการปัดเศษขึ้น/ลงในฝั่งจำนวนเต็ม (หลักหน่วย หลักสิบ หลักร้อย ไปจนถึงหลักพัน)
--   - เช่น ตัวเลข 54,320 เมื่อเจอ ROUND(..., -3) จะถูกปัดเศษลงมาเหลือ 54,000 ทันที
-- --------------------------------------------------------

SELECT name, ROUND(gdp/population, -3) AS per_capita_gdp
FROM world
WHERE gdp >= 1000000000000;


-- --------------------------------------------------------
-- Quiz 6: Vowel Filtering (Advanced Text Matching using LIKE)
-- โจทย์: ค้นหาประเทศที่มีสระครบทุกตัว (a, e, i, o, u) ในชื่อ และต้องไม่มีช่องว่าง (ชื่อคำเดียว)
-- เทคนิคจากลายมือ: ใช้คำสั่ง `LIKE '%a%'` ซ้อนกันหลายบรรทัดด้วย AND เพื่อบังคับให้มีสระครบ 
--             และตบท้ายด้วย `NOT LIKE '% %'` เพื่อสกัดประเทศที่มีคำเว้นวรรคออกไป
-- --------------------------------------------------------

SELECT name 
FROM world
WHERE name LIKE '%a%'
  AND name LIKE '%e%'
  AND name LIKE '%i%'
  AND name LIKE '%o%'
  AND name LIKE '%u%'
  AND name NOT LIKE '% %';
